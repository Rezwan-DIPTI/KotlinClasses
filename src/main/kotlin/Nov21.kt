fun main(args: Array<String>) {
    //Float
    var num1:Float = 12.5f
    var num2:Float = 10.2f
    println(num1+num2)

    //Double
    var num3:Double =12.5
    var num4:Double =10.2
    println("The Sum is: ${num3+num4}")

    //Character
    var ch:Char = 'A'
    println(ch)

    //Boolean
    var isMarried:Boolean = true
    println(isMarried)
    var haveChild:Boolean = false
    println(haveChild)
}

